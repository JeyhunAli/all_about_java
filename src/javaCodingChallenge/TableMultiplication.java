package javaCodingChallenge;

public class TableMultiplication {

	public static void main(String[] args) {

		//table multiplication means ---> 3 * starting from one up to specific defined number 
		// for example 
		/**
		 *    3 * 1 = 3
              3 * 2 = 6
              3 * 3 = 9
              3 * 4 = 12
		 */
		
		
		
		
		int num = 9;

		for (int i = 1; i <= 10; i++) {

			System.out.println(num + " * " + i + " = " + (num * i));

		}

	}

}
