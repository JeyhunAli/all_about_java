package sep2020JavaSession;

public class oneTOten {

	public static void main(String[] args) {

		// WAP to print 10 to 1 using 
		// for,
		// while
		// do-while loop
		
		
		int i=0;
		do {
			System.out.println("D0 while LOOP "+i);
			i++;
		}
		while(i<=10);
		
		System.out.println("*********************************");
		
		
		for(int y=0; y<=10; y++) {
			System.out.println("FORL00P "+y);
		}
		
		System.out.println("*********************************");

		
		int num = 0;
		while(num<=10) {
			System.out.println("while L00P " +num);
			num++;
		}
		
		
	}

}
